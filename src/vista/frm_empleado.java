    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        //Empleado obj_empleado = new Empleado();
        if(cbx_puesto.getSelectedIndex() == 0){
            int ejecutar = 0;
            JOptionPane.showMessageDialog(null, Integer.toString(ejecutar)+ "DEBE ESCOGER UN PUESTO.","Agregar", JOptionPane.INFORMATION_MESSAGE);
        }else{
        obj_empleado.setId_empleado(Integer.parseInt(this.lbl_id.getText()));
        obj_empleado.setCodigo(this.txt_empCod.getText());
        obj_empleado.setNombre(this.txt_empNombres.getText());
        obj_empleado.setApellido(this.txt_empApellidos.getText());
        obj_empleado.setDireccion(this.txt_empDireccion.getText());
        obj_empleado.setTelefono(this.txt_empTelefono.getText());
        obj_empleado.setFechanacimiento(this.txt_empFechaNac.getText());
        obj_empleado.setId_puesto(cbx_puesto.getSelectedIndex()-1);
        
        obj_empleado.actualizar();
        tbl_empleado.setModel(obj_empleado.leer());
        }

    }//GEN-LAST:event_btn_updateActionPerformed

