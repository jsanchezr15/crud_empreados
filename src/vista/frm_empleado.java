    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //Empleado obj_empleado = new Empleado();
        if(cbx_puesto.getSelectedIndex() == 0){
            int ejecutar = 0;
            JOptionPane.showMessageDialog(null, Integer.toString(ejecutar)+ "DEBE ESCOGER UN PUESTO.","Agregar", JOptionPane.INFORMATION_MESSAGE);
        }else{
            obj_empleado.setCodigo(this.txt_empCod.getText());
            obj_empleado.setNombre(this.txt_empNombres.getText());
            obj_empleado.setApellido(this.txt_empApellidos.getText());
            obj_empleado.setDireccion(this.txt_empDireccion.getText());
            obj_empleado.setTelefono(this.txt_empTelefono.getText());
            obj_empleado.setFechanacimiento(this.txt_empFechaNac.getText());
            obj_empleado.setId_puesto(cbx_puesto.getSelectedIndex()-1);

            obj_empleado.agregar(); 
            tbl_empleado.setModel(obj_empleado.leer());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

        public void select_datos(){
            int fila = tbl_empleado.getSelectedRow();
            lbl_id.setText(tbl_empleado.getValueAt(fila,0).toString());
            txt_empCod.setText(tbl_empleado.getValueAt(fila,1).toString());
            txt_empNombres.setText(tbl_empleado.getValueAt(fila,2).toString());
            txt_empApellidos.setText(tbl_empleado.getValueAt(fila,3).toString());
            txt_empDireccion.setText(tbl_empleado.getValueAt(fila,4).toString());
            txt_empTelefono.setText(tbl_empleado.getValueAt(fila,5).toString());
            txt_empFechaNac.setText(tbl_empleado.getValueAt(fila,6).toString());
            jButton1.setEnabled(false);
            btn_update.setEnabled(true);
            btn_eliminar.setEnabled(true);
        }
    
